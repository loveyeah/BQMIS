package power.ejb.resource.form;

public class InquirePriceBean {
	
	private String inquireQty;
	private String unitPrice;
	private String unitName;
	private String unitId;
	/**
	 * @return the inquireQty
	 */
	public String getInquireQty() {
		return inquireQty;
	}
	/**
	 * @param inquireQty the inquireQty to set
	 */
	public void setInquireQty(String inquireQty) {
		this.inquireQty = inquireQty;
	}
	/**
	 * @return the unitPrice
	 */
	public String getUnitPrice() {
		return unitPrice;
	}
	/**
	 * @param unitPrice the unitPrice to set
	 */
	public void setUnitPrice(String unitPrice) {
		this.unitPrice = unitPrice;
	}
	/**
	 * @return the unitName
	 */
	public String getUnitName() {
		return unitName;
	}
	/**
	 * @param unitName the unitName to set
	 */
	public void setUnitName(String unitName) {
		this.unitName = unitName;
	}
	/**
	 * @return the unitId
	 */
	public String getUnitId() {
		return unitId;
	}
	/**
	 * @param unitId the unitId to set
	 */
	public void setUnitId(String unitId) {
		this.unitId = unitId;
	}
	

}
