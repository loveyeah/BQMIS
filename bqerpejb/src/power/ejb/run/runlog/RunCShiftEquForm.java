package power.ejb.run.runlog;

public class RunCShiftEquForm {

	private String runEquId;
	private String specialityName;
	private String runwayName;
	private String attributeCode;
	private String equName;
	private String isUse;
	private String enterpriseCode;

	public String getSpecialityName() {
		return specialityName;
	}
	public void setSpecialityName(String specialityName) {
		this.specialityName = specialityName;
	}
	public String getRunwayName() {
		return runwayName;
	}
	public void setRunwayName(String runwayName) {
		this.runwayName = runwayName;
	}
	public String getAttributeCode() {
		return attributeCode;
	}
	public void setAttributeCode(String attributeCode) {
		this.attributeCode = attributeCode;
	}
	public String getEquName() {
		return equName;
	}
	public void setEquName(String equName) {
		this.equName = equName;
	}
	public String getIsUse() {
		return isUse;
	}
	public void setIsUse(String isUse) {
		this.isUse = isUse;
	}
	public String getEnterpriseCode() {
		return enterpriseCode;
	}
	public void setEnterpriseCode(String enterpriseCode) {
		this.enterpriseCode = enterpriseCode;
	}
	public String getRunEquId() {
		return runEquId;
	}
	public void setRunEquId(String runEquId) {
		this.runEquId = runEquId;
	}
}
